class java10 {
    public static void main(String args[])
    {
        int a,b,c;
        a=100;
        b=50;
        c=a+b;
        System.out.println("Addition: "+c);
        c=a-b;
        System.out.println("Subtraction: "+c);
        c=a*b;
        System.out.println("Multiplication: "+c);
        c=a/b;
        System.out.println("Division: "+c);
        c='b'%'a';
        System.out.println("Remainder: "+c);
        c='z'%'A';
        System.out.println("Remainder: "+c);
    }
}
