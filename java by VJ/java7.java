//Mathematical Functions in Java
class java7 {

  public static void main(String args[]) {
    int m = 20, n = 20;
    System.out.println("The minimum Value:          " + Math.min(m, n));
    System.out.println("The maximum Value:          " + Math.max(m, n));
    System.out.println("Square Root of 9:           " + Math.sqrt(m));
    System.out.println("Power of 2 and 3:           " + Math.pow(2, 3));
    System.out.println("Exponential of 74.556 is:   " + Math.exp(73.556));
    System.out.println("Round (200.634):            " + Math.round(200.634));
    System.out.println("Absolute Value:             " + Math.abs(-586));
  }
}
