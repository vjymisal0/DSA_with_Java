//bitwise operators
class java13 {

  public static void main(String args[]) {
    System.out.println("Bitwise  And 12 & 13: " + (12 & 13));
    System.out.println("Bitwise  Or 12 | 13: " + (12 | 13));
    System.out.println("Bitwise  XOR 12 ^ 13: " + (12 ^ 13));
    System.out.println("Bitwise  NOT ~14    : " + (~14));
    System.out.println("Bitwise  Right Shift: " + (14 >> 2));
    System.out.println("Bitwise  Left Shift: " + (14 << 2));
  }
}
