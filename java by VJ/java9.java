//type casting explicitly
class java9 {

  public static void main(String args[]) {
    float a = 70;
    int b;
    b =(int) a;
    System.out.println("Value of Float variable: " + a);
    System.out.println("Value of Int Variable: " + b);
  }
}
/*
    Java contains default value to the variables that are not initialized
    Types of Variables           Default Value
    byte                            zero    
    short                           zero
    int                             zero
    long                            zero                
    float                           0.0f
    double                          0.0d
    char                            Null Character
    boolean                         False
*/