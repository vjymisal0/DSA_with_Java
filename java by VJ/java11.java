class java11 {

  public static void main(String args[]) {
    System.out.println("10<20: " + (10 < 20));
    System.out.println("10>20: " + (10 > 20));
    System.out.println("10<=20: " + (10 <= 20));
    System.out.println("45>=45: " + (45 >= 45));
    System.out.println("20==20: " + (20 == 20));
    System.out.println("10!=20: " + (10 != 20));
  }
}
