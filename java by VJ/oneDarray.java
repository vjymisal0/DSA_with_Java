class oneDarray {

  public static void main(String args[]) {
    int a[] = new int[5];
    a[0] = 10;
    a[1] = 20;
    a[2] = 30;
    a[3] = 40;
    a[4] = 50;
    System.out.println("Array Elements: a[0]: " + a[0]);
    System.out.println("Array Elements: a[1]: " + a[1]);
    System.out.println("Array Elements: a[2]: " + a[2]);
    System.out.println("Array Elements: a[3]: " + a[3]);
    System.out.println("Array Elements: a[4]: " + a[4]);
    int len=a.length;
    System.out.println("length of array: "+len);
  }
}
